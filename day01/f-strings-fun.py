a = 100
b = 12.234582

# 格式化输出
c = f"{a}-{b}"
print(c)
# 100-12.234582

# 直接以a=的形式打印值
print(f'{a=}')
# a=100

# 宽度为10，右对齐，用空格填充
print(f'{a:10}')
#       100

# 宽度为10，右对齐，用_填充
print(f"{a:_>10}")

# 宽度为10，左对齐，用:填充
print(f'{a::<10}')
# 100:::::::

# 宽度为10，右对齐，用空格填充，且强制转换为整数
d = True
print(f'{d:10d}')
#         1

# 右对齐，用空格填充，且保留2位小数
e = 10
print(f'{e:.2f}')
# 10.00

f = 20
# 右对齐，转换为16进制，且在前面添加0x
zz = f'{e:#0x}'
print(zz)
# 0x14

# 右对齐，转换为16进制，且不添加0x
yy = f'{f:0x}'
print(yy)
# 14
