# 算术运算符的基本使用

# + 加法运算符
print(1 + 2)  # 输出: 3

# - 减法运算符
print(2 - 1)  # 输出: 1

# * 乘法运算符
print(2 * 3)  # 输出: 6

# / 除法运算符
print(5 / 5)  # 输出: 1.0

# // 取整除-返回商的整数部分（向下取整）
print(9 // 5)  # 输出: 1

# % 取余运算符
print(9 % 5)  # 输出: 4

# ** 幂运算符, 返回 x 的 y 次幂
print(2 ** 3)  # 输出: 8

# 假如还有97天放假，问：合xx个星期 零xx天
day = 97
week = day // 7
left_day = day % 7
print(f"假如还有{day}, 合{week}星期零{left_day}天")


